package Methods;

public class MoreAboutFunction {

    public static void fun() {
        System.out.println("Hii, Everyone");
    }

    public static void ravi() {
        System.out.println("My name is Ravi");
    }

    public static void radha() {
        System.out.println("My name is Radha");
    }
    public static void main(String[] args) {
        System.out.println("Hii ");
        fun();
        System.out.println("Radha ");
        ravi();
        radha();
    }
}
